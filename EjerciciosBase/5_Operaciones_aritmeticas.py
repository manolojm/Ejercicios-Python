# 5_Operaciones_aritmeticas

numero1 = float(input('Introduzca el primer numero: '));
numero2 = float(input('Introduzca el segundo numero: '));

resultado_suma = numero1 + numero2;
resultado_resta = numero1 - numero2;
resultado_multiplicacion = numero1 * numero2;
resultado_division = numero1 / numero2;

print('La suma de los numeros es: ' , resultado_suma);
print('La resta de los numeros es: ' , resultado_resta);
print('La multiplicacion de los numeros es: ' , resultado_multiplicacion);
print('La division de los numeros es: ' , resultado_division);
