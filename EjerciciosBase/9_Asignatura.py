diaSemana = input('Introduzca dia de la semana: ')

if diaSemana == "lunes":
    resultado = "A primera hora tenemos matematicas"
elif diaSemana == "martes":
    resultado = "A primera hora tenemos lengua"
elif diaSemana == "miercoles":
    resultado = "A primera hora tenemos ingles"
elif diaSemana == "jueves":
    resultado = "A primera hora tenemos gimnasia"
elif diaSemana == "viernes":
    resultado = "A primera hora tenemos frances"
else:
    resultado = "Dia incorrecto"
                        
print(resultado)                        
            
